        unsigned short int getTrackState(unsigned int trackId, int traveled) {
            if (currentTrackId != trackId) {
                currentTrack = TrackBuilder::buildTrack(trackId);
                currentTrackId = trackId;
            }
            std::list<unsigned short int>::iterator iterator = currentTrack.begin();
            std::advance(iterator, traveled);
            return *iterator;
        }

